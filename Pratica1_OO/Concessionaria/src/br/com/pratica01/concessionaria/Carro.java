package br.com.pratica01.concessionaria;
/**
 * Carro
 */
public class Carro {
    private String marca;
    private String modelo;
    private int ano;
    private double valor;
    private String cor;
    private String chassi;
    
    public Carro(String marca, String modelo, int ano, double valor, String cor, String chassi) {
        this.marca = marca;
        this.modelo = modelo;
        this.ano = ano;
        this.valor = valor;
        this.cor = cor;
        this.chassi = chassi;
    }


   
    
}